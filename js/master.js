/** Diferencias entre Null, Undefined y Void */
let mascota = '';    // Nulo 

if( mascota === null ) {        // Pocas veces usado
    console .log( 'Es nulo' );
}
if ( mascota === undefined ) {      // Nunca lo usas
    console .log( 'Esta indefinido' );
}
if ( mascota === '' ) {         // Si lo usas
    console .log( 'Esta vacio' );
}